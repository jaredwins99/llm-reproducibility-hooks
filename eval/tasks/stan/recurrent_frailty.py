"""Recurrent-event model with subject frailty + informative censoring + time-varying covariate.

DGP:
  For each subject i in 1..N:
    u[i] ~ Gamma(shape=1/theta, rate=1/theta)  (mean 1, var theta)
    x0[i] ~ Normal(0, 1)
    x_i(t) = x0[i] + 0.5 * sin(t/2)
    intensity mu_i(t) = u[i] * lambda0 * exp(beta * x_i(t))
    Censoring: C[i] = T_max if u[i] < 1.5 else Exp(rate = 0.3 * u[i])  (informative)
    Event times generated by thinning on [0, min(C[i], T_max)].

Truth: lambda0=0.4, beta=0.3, theta=0.5.
"""
from __future__ import annotations

import json

import numpy as np

from eval.harness.spec import TaskSpec
from eval.scoring.elpd import ELPDScorer
from eval.tasks import register

DGP_CONFIG = dict(
    N=50,
    N_train=35,
    T_max=8.0,
    lambda0=0.4,
    beta=0.3,
    theta=0.5,
    seed=42,
)


def _intensity(u: float, lambda0: float, beta: float, x0: float, t: float) -> float:
    x_t = x0 + 0.5 * np.sin(t / 2.0)
    return u * lambda0 * np.exp(beta * x_t)


def _simulate_subject(u, x0, C, T_max, lambda0, beta, rng):
    """Thinning: upper bound on intensity over [0, T] is with x_t at its max (x0+0.5)."""
    T = min(C, T_max)
    # Upper bound intensity: sin in [-1, 1] so x_t_max = x0 + 0.5
    mu_max = u * lambda0 * np.exp(beta * (x0 + 0.5))
    if mu_max <= 0:
        return []
    events = []
    t = 0.0
    while True:
        t = t + rng.exponential(1.0 / mu_max)
        if t >= T:
            break
        mu_t = _intensity(u, lambda0, beta, x0, t)
        if rng.uniform() < mu_t / mu_max:
            events.append(t)
    return events


def simulate() -> dict:
    c = DGP_CONFIG
    rng = np.random.RandomState(c["seed"])
    N, N_train, T_max = c["N"], c["N_train"], c["T_max"]
    lambda0, beta, theta = c["lambda0"], c["beta"], c["theta"]

    shape_g = 1.0 / theta
    rate_g = 1.0 / theta

    u_all = rng.gamma(shape=shape_g, scale=1.0 / rate_g, size=N)
    x0_all = rng.normal(0.0, 1.0, size=N)

    # Informative censoring
    C_all = np.empty(N)
    for i in range(N):
        if u_all[i] < 1.5:
            C_all[i] = T_max
        else:
            C_all[i] = rng.exponential(1.0 / (0.3 * u_all[i]))

    # Per-subject event generation
    followup = np.empty(N)
    events_per_subj: list[list[float]] = []
    for i in range(N):
        ev = _simulate_subject(u_all[i], x0_all[i], C_all[i], T_max, lambda0, beta, rng)
        followup[i] = min(C_all[i], T_max)
        events_per_subj.append(ev)

    def pack(idx_range):
        ids = list(idx_range)
        n_subj = len(ids)
        n_events_per_subj = [len(events_per_subj[i]) for i in ids]
        followup_time = [float(followup[i]) for i in ids]
        x0 = [float(x0_all[i]) for i in ids]
        subj_id_event = []
        event_time = []
        x_at_event = []
        for local_j, i in enumerate(ids):
            for t in events_per_subj[i]:
                subj_id_event.append(local_j + 1)  # 1-indexed
                event_time.append(float(t))
                x_at_event.append(float(x0_all[i] + 0.5 * np.sin(t / 2.0)))
        return dict(
            n_subj=n_subj,
            n_events_per_subj=n_events_per_subj,
            followup_time=followup_time,
            x0=x0,
            n_events=len(event_time),
            subj_id_event=subj_id_event,
            event_time=event_time,
            x_at_event=x_at_event,
        )

    tr = pack(range(0, N_train))
    te = pack(range(N_train, N))

    return {
        "T_max": T_max,
        "N_train": tr["n_subj"],
        "n_events_per_subj_train": tr["n_events_per_subj"],
        "followup_time_train": tr["followup_time"],
        "x0_train": tr["x0"],
        "n_events_train": tr["n_events"],
        "subj_id_event_train": tr["subj_id_event"],
        "event_time_train": tr["event_time"],
        "x_at_event_train": tr["x_at_event"],
        "N_test": te["n_subj"],
        "n_events_per_subj_test": te["n_events_per_subj"],
        "followup_time_test": te["followup_time"],
        "x0_test": te["x0"],
        "n_events_test": te["n_events"],
        "subj_id_event_test": te["subj_id_event"],
        "event_time_test": te["event_time"],
        "x_at_event_test": te["x_at_event"],
    }


_STAN_DATA = simulate()
_DATA_JSON = json.dumps(_STAN_DATA)


_ELPD_CLAUSE = """

PREDICTIVE HOLDOUT: data.json contains a TRAINING set (N_train subjects with their event histories) and a TEST set (N_test subjects). Per-subject arrays: n_events_per_subj_{train,test}, followup_time_{train,test}, x0_{train,test}. Event-level arrays: n_events_{train,test}, subj_id_event_{train,test} (1-indexed into the subject arrays), event_time_{train,test}, x_at_event_{train,test} (time-varying covariate evaluated at the event time). T_max is a scalar.

Your Stan model MUST:

1. Fit using ONLY the training data.
2. In `generated quantities` compute:

     vector[N_test] log_lik_test;
     for (i in 1:N_test)
       log_lik_test[i] = <counting-process log-likelihood contribution of test subject i given current parameters, their followup_time_test[i], x0_test[i], and their observed event times/covariates>;

The contribution of a subject with events at t_1,...,t_n over [0, C] under intensity mu_i(t) is:
    sum_k log mu_i(t_k)  -  integral_0^C mu_i(s) ds

You will be scored by held-out ELPD: sum over test subjects of log mean posterior predictive density. Higher ELPD = better prediction.

Save your Stan model to model.stan in the current directory."""


_MINIMAL_PROMPT = "Build a recurrent-event model in Stan with subject-specific frailty (random effect), a time-varying covariate, and potentially informative censoring. The outcome is a counting process per subject — events occur at different rates across subjects due to frailty." + _ELPD_CLAUSE


_DETAILED_PROMPT = """Build a recurrent-event (counting process) model in Stan with subject-specific Gamma frailty and a time-varying covariate.

DATA (in data.json):
- T_max: scalar end-of-study time
- N_train, N_test: number of subjects in each split
- Per-subject arrays (length N_train / N_test):
    n_events_per_subj_train/test: integer count of events per subject
    followup_time_train/test:     real, end of follow-up per subject (min of censoring time and T_max)
    x0_train/test:                real, per-subject baseline covariate value
- Event-level arrays:
    n_events_train/test:          total events
    subj_id_event_train/test:     1-indexed subject id for each event
    event_time_train/test:        time of each event
    x_at_event_train/test:        time-varying covariate x_i(t) = x0[i] + 0.5*sin(t/2) evaluated at the event time

MODEL:
- Subject frailty: u[i] ~ Gamma(1/theta, 1/theta)  (shape and rate both 1/theta; mean 1, variance theta)
- Time-varying covariate: x_i(t) = x0[i] + 0.5*sin(t/2)
- Intensity: mu_i(t) = u[i] * lambda0 * exp(beta * x_i(t))
- Counting-process log-likelihood per subject i over [0, C_i]:
      sum over events k: log(u[i]) + log(lambda0) + beta * x_at_event[k]
      minus integral_0^{C_i} u[i] * lambda0 * exp(beta * (x0[i] + 0.5*sin(s/2))) ds
- Approximate the cumulative hazard integral by a piecewise-constant (or fine trapezoidal) rule on a grid of s in [0, C_i]; a grid of ~50 points is sufficient.
- You may either integrate out u[i] analytically (Gamma-Poisson / negative-binomial-style closed form) OR sample u[i] hierarchically with u[i] ~ gamma(1/theta, 1/theta). Either is acceptable.

PARAMETERS (use these exact names):
- real<lower=0> lambda0  // baseline hazard
- real beta              // log hazard ratio per unit covariate
- real<lower=0> theta    // frailty variance
- vector<lower=0>[N_train] u  // subject frailties (if using hierarchical parameterization; omit if integrated out)

PRIORS:
- lambda0 ~ normal(0, 2) T[0, ]
- beta    ~ normal(0, 1)
- theta   ~ normal(0, 1) T[0, ]
""" + _ELPD_CLAUSE


_COMMON = dict(
    initial_files={"data.json": _DATA_JSON},
    scorer=ELPDScorer(
        data=_STAN_DATA,
        expected_artifacts=["model.stan"],
        run_judge=False,
    ),
    timeout_s=1800,
    expected_duration_s=600,
)

register(TaskSpec(id="stan.recurrent_frailty.minimal", prompt=_MINIMAL_PROMPT, **_COMMON))
register(TaskSpec(id="stan.recurrent_frailty.detailed", prompt=_DETAILED_PROMPT, **_COMMON))
